n = int(input())

max_sum = 2 * n - 1

# Находим максимальное количество девяток на конце.
# Это эквивалентно поиску наибольшего mod = 10^k, такого что mod - 1 <= max_sum.
# Число mod - 1 состоит из k девяток. Если оно <= max_sum, значит сумма из k девяток достижима.
# Если mod - 1 > max_sum, то k девяток на конце получить нельзя.
mod = 1
while (mod * 10 - 1) <= max_sum:
    mod *= 10
    
# Если mod == 1, это значит, что максимальное количество девяток равно 0.
# В этом случае любая пара лопат подходит (так как 0 девяток — это тривиальное условие).
# Количество пар из n элементов равно n * (n - 1) / 2.
if mod == 1:
    print(n * (n - 1) // 2)
else:
    count = 0
    # Нам нужно посчитать пары (i, j), такие что 1 <= i < j <= n
    # и (i + j) заканчивается на k девяток.
    # Это значит (i + j) % mod == mod - 1.
    # Возможные суммы S: mod-1, 2*mod-1, 3*mod-1, ...
    # Перебираем все возможные суммы S <= max_sum.
    # Так как mod примерно равен n (или n/2, n/10...), цикл выполнится очень мало раз (<= 20).
    
    s = mod - 1
    while s <= max_sum:
        
        min_i = max(1, s - n)
        max_i = (s - 1) // 2
        
        if max_i >= min_i:
            count += (max_i - min_i + 1)
        
        s += mod
        
    print(count)